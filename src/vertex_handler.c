static bool extract_vertex(t_vector *vertices, char *line)
{
	float vertex[3];

	if (!line)
	return false;

	//ensure line starts with "v "
	if (strcmp("v ", line) != 0)
		return false;
	line += 2;

	//first float
	if (!is_float(line))
		return false;
	vertex[0] = atof(line);
	line = skip_float(line);
	
	//ensure float is followed by a ' '
	if (*line != ' ')
		return false;
	line++;

	//second float
	if (!is_float(line))
		return false;
	vertex[1] = atof(line);
	line = skip_float(line);

	//ensure float is followed by a ' '
	if (*line != ' ')
		return false;
	line++;

	//third float
	if (!is_float(line))
		return false;
	vertex[2] = atof(line);
	line = skip_float(line);

	if (*line == '\n' || *line == '\0')
	{
		if (vertices->used + sizeof(vertex) > vertices->cap)
			vector_resize(vertices, vertices->cap * 2);
		memset(vertices->vec + vertices->used, vertex, sizeof(vertex));
		vertices->used += sizeof(vertex);
		return true;
	}
	else
		return false;
}

